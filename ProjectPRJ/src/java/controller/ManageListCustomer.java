package controller;

import dao.CategoryDAO;
import dao.CustomerDAO;
import java.io.IOException;
import jakarta.servlet.ServletException;
import jakarta.servlet.annotation.WebServlet;
import jakarta.servlet.http.HttpServlet;
import jakarta.servlet.http.HttpServletRequest;
import jakarta.servlet.http.HttpServletResponse;
import java.util.List;
import model.Category;
import model.Customer;

/**
 *
 * @author Admin
 */
@WebServlet(name = "ManageListCustomer", urlPatterns = {"/manageListCustomer"})
public class ManageListCustomer extends HttpServlet {

    protected void processRequest(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        response.setContentType("text/html;charset=UTF-8");

    }

    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
            CustomerDAO customerDao = new CustomerDAO();
            List<Customer> list = customerDao.getAllCustomer();
            request.setAttribute("listCustomer", list);
            
            CategoryDAO cd = new CategoryDAO();
            List<Category> listCategory = cd.getAllCategory();
            request.setAttribute("listCategory", listCategory);
            
            request.getRequestDispatcher("manageListCustomer.jsp").forward(request, response);
    }

    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        processRequest(request, response);
    }

    @Override
    public String getServletInfo() {
        return "Short description";
    }// </editor-fold>

}
